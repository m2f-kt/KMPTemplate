#!/usr/bin/env node

// PreToolUse hook: Block edits to generated/lock files and committed migrations
const input = process.env.CLAUDE_TOOL_INPUT || "";

try {
  const parsed = JSON.parse(input);
  const filePath = parsed.file_path || parsed.path || "";

  // Block Gradle lock files
  if (/gradle\.lockfile$/.test(filePath) || /\.lock$/.test(filePath) && /gradle/.test(filePath)) {
    process.stderr.write(
      "BLOCKED: Gradle lock files are generated and must not be edited manually. " +
      "Run './gradlew dependencies --write-locks' to update them.\n"
    );
    process.exit(2);
  }

  // Block gradle-wrapper.properties (should be updated via `gradle wrapper`)
  if (/gradle-wrapper\.properties$/.test(filePath)) {
    process.stderr.write(
      "BLOCKED: gradle-wrapper.properties should be updated via './gradlew wrapper --gradle-version=X.Y'. " +
      "Do not edit manually.\n"
    );
    process.exit(2);
  }

  // Block gradlew and gradlew.bat (generated by Gradle wrapper)
  if (/\/gradlew(\.bat)?$/.test(filePath)) {
    process.stderr.write(
      "BLOCKED: gradlew scripts are generated by the Gradle wrapper. Do not edit manually.\n"
    );
    process.exit(2);
  }
} catch {
  // If input is not valid JSON, skip check
}
